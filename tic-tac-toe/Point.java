package tic_tac_toe;

public class Point {
	
	//Position for the block in the board
	public int x,y;
	
/*********************************************************************************************************************
 * The constructor takes the x and y coordinates of the tic tac toe block
 * @param x
 * @param y
 */
	public Point(int x, int y) {
		this.x = x;
		this.y = y;
	}
}
